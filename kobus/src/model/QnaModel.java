package model;

public class QnaModel {
	private int a_id;
	private int g_id;
	private int s_id;
	private String q_contnent;
	private String q_date;
	private int q_hit;
	private String m_id;
	
	public QnaModel() {
		
	}
	
	public int getA_id() {
		return a_id;
	}
	public void setA_id(int a_id) {
		this.a_id = a_id;
	}
	public int getG_id() {
		return g_id;
	}
	public void setG_id(int g_id) {
		this.g_id = g_id;
	}
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getQ_contnent() {
		return q_contnent;
	}
	public void setQ_contnent(String q_contnent) {
		this.q_contnent = q_contnent;
	}
	public String getQ_date() {
		return q_date;
	}
	public void setQ_date(String q_date) {
		this.q_date = q_date;
	}
	public int getQ_hit() {
		return q_hit;
	}
	public void setQ_hit(int q_hit) {
		this.q_hit = q_hit;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	
}
